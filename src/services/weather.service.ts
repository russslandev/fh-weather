export const WeatherService = {
  async getAllData({ lat, lon }: { lat: number; lon: number }) {
    return axios.get('/data/2.5/forecast', {
      params: {
        lat,
        lon,
        appid: process.env.REACT_APP_API_KEY,
        exclude: 'hourly',
        units: 'metric',
      },
    });
  },
};

